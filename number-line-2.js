function numLineOne() {
var resultOne = '27';
var userInputOne = document.getElementById("num-line2-1").value;
var resultTwo = '30';
var userInputTwo = document.getElementById("num-line2-1-2").value;
if (userInputOne === resultOne && userInputTwo === resultTwo) {
document.getElementById("tickcontainerNumberLines2-1").style.display = "flex";
}else {
	document.getElementById("tickcontainerNumberLines2-1").style.display = "none";
}
};

